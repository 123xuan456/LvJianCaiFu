package com.ryan.slidefragment.domain;


public class TechnologyShousBean{

	private String cheng;

	private int code;

	private String imgurl;

	private String jian;

	private String name;

	public void setCheng(String cheng){
	this.cheng = cheng;
	}
	public String getCheng(){
	return this.cheng;
	}
	public void setCode(int code){
	this.code = code;
	}
	public int getCode(){
	return this.code;
	}
	public void setImgurl(String imgurl){
	this.imgurl = imgurl;
	}
	public String getImgurl(){
	return this.imgurl;
	}
	public void setJian(String jian){
	this.jian = jian;
	}
	public String getJian(){
	return this.jian;
	}
	public void setName(String name){
	this.name = name;
	}
	public String getName(){
	return this.name;
	}

}
